def generate_permit_note(permit: dict, now_str: str) -> str:
    pid = permit["id"]
    category = permit["category"]
    tags = permit.get("tags", ["인허가"])
    tags_yaml = ", ".join(tags)

    frontmatter = f"""---
type: permit-guide
category: "{category}"
updated: {now_str}
tags: [{tags_yaml}]
---

"""
    footer_links = "\n".join(
        f"- [[{r}]]" for r in permit.get("related", [])
    )
    footer = f"""

---

## 관련 노트

{footer_links}

---

> **법제처 바로가기**: [국가법령정보센터](https://www.law.go.kr)
"""
    return frontmatter + permit["content"] + footer


def generate_permit_index(permits: list, now_str: str) -> str:
    rows = "\n".join(
        f"| [[{p['id']}]] | {p['category']} | {', '.join(p.get('tags', [])[:2])} |"
        for p in permits
    )
    content = f"""---
type: index
category: "L. 인허가 절차"
updated: {now_str}
tags: [인허가, INDEX]
---

# 인허가 절차 INDEX

> [[인허가_흐름도]] — 전체 프로세스 통합 안내

## 노트 목록

| 노트 | 카테고리 | 태그 |
|------|----------|------|
{rows}

## 법령 바로가기

- [[건축법]] — 건축허가, 신고, 대수선, 용도변경, 사용승인
- [[국토계획법]] — 개발행위허가
- [[경관법]] — 경관심의
- [[건축물관리법]] — 해체허가·신고
- [[자연재해대책법]] — 사전재해영향성검토
- [[도시교통정비촉진법]] — 교통영향평가
"""
    return content


def run_permit_notes(force_overwrite: bool = True) -> None:
    PERMIT_FOLDER.mkdir(parents=True, exist_ok=True)
    now_str = date.today().isoformat()

    created = 0
    skipped = 0

    # 개별 노트 생성
    for permit in PERMITS:
        path = PERMIT_FOLDER / f"{permit['id']}.md"
        if path.exists() and not force_overwrite:
            print(f"  SKIP  {path.name}")
            skipped += 1
            continue
        note_text = generate_permit_note(permit, now_str)
        path.write_text(note_text, encoding="utf-8")
        print(f"  OK    {path.name}  ({len(note_text):,} chars)")
        created += 1

    # INDEX 생성
    index_path = PERMIT_FOLDER / "INDEX_인허가절차.md"
    if not index_path.exists() or force_overwrite:
        index_path.write_text(generate_permit_index(PERMITS, now_str), encoding="utf-8")
        print(f"  OK    {index_path.name}")
        created += 1

    print(f"\n완료: {created}개 생성, {skipped}개 건너뜀")
    print(f"저장 위치: {PERMIT_FOLDER}")


if __name__ == "__main__":
    run_permit_notes()
